package data_types;

public class MIS2DTransform {

	public double positionX;
	public double positionY;
	public double rotation;
	public double scaleX;
	public double scaleY;
	
	public MIS2DTransform(double positionX, double positionY, double rotation, double scaleX, double scaleY){
		this.positionX = positionX;
		this.positionY = positionY;
		this.rotation = rotation;
		this.scaleX = scaleX;
		this.scaleY = scaleY;
	}
	
	
}
